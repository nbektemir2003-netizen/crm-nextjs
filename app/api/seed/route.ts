export async function GET() {
  // Seed admin user
  const hash = await bcrypt.hash('admin123', 10)
  const { data: existing } = await supabase.from('User').select('id').eq('email', 'admin@crm.kz').single()
  if (existing) {
    await supabase.from('User').update({ passwordHash: hash }).eq('email', 'admin@crm.kz')
  } else {
    await supabase.from('User').insert([{ id: crypto.randomUUID(), email: 'admin@crm.kz', name: 'Администратор', passwordHash: hash, role: 'admin' }])
  }

  // Clear and re-seed companies
  await supabase.from('Company').delete().neq('id', '00000000-0000-0000-0000-000000000000')

  const coRows = DEFAULT_COMPANIES.map(r => ({
    id: crypto.randomUUID(),
    name: r[0],
    serviceGroup: r[1],
    taxMode: r[2],
    category: r[3],
    has1c: r[4] === 'БАР',
    risk: r[5],
    hasVat: r[6] === 'Да',
    status: r[7],
  }))
  const { error: coErr } = await supabase.from('Company').insert(coRows)
  if (coErr) return Response.json({ error: 'Company seed failed: ' + coErr.message }, { status: 500 })

  // Clear and re-seed tasks
  await supabase.from('Task').delete().neq('id', '00000000-0000-0000-0000-000000000000')

  const taskRows = DEFAULT_TASKS.map(r => ({
    id: crypto.randomUUID(),
    companyName: r[0],
    description: r[1],
    assigneeName: r[2],
    priority: r[3],
    deadline: r[4] + 'T00:00:00',
    status: r[5],
  }))
  const { error: taskErr } = await supabase.from('Task').insert(taskRows)
  if (taskErr) return Response.json({ error: 'Task seed failed: ' + taskErr.message }, { status: 500 })

  return Response.json({
    message: `Seed complete: ${coRows.length} companies, ${taskRows.length} tasks. Admin: admin@crm.kz / admin123`
  })
}
